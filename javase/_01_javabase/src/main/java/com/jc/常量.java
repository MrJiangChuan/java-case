package com.jc;

/**
 * 常量
 * 1.布尔常量：true false
 * 2.字符常量：'a'
 * 3.整型常量：12（默认是int类型） 123133L（long类型）
 * 4.浮点常量：12.442(默认是double类型) 12.44F（float类型）
 * 5.字符串常量："abcde"
 * 6.空常量：null
 */
public class 常量 {

    public static void main(String[] args) {

        //1.布尔常量
        System.out.println(true);
        System.out.println(false);

        //2.字符常量
        System.out.println('a');
        System.out.println(' ');
//        System.out.println(''); 错误写法

        //3.整型常量
        System.out.println(12);
        System.out.println(1234141414L);

        //4.浮点常量
        System.out.println(12.442);
        System.out.println(12.44F);

        //5.字符串常量
        System.out.println("abcde");
        System.out.println("");

        //6.空常量
//        System.out.println(null);错误写法

    }
}
